			    <p>  In this experiment we introduce you to two very famous algorithmic problems.One problem is longest continuous decreasing subsequence problem and other one is longest increasing subsequence problem. </p> <br>  <h2>Problem 1:</h2> <p>  Given a list of n distinct numbers (where n &lt= 1000), find the length of the longest monotone decreasing subsequence. </p> <p> Definition : A monotone decreasing subsequence is a sequence of numbers (contiguously placed in the list) which are strictly decreasing from left to right. For example, if we have the series ”2 1 9 8 10 7 5 4 3 1 10”, then the subsequence ”10 7 5 4 3 1” is contiguously arranged and monotonically decreasing. Thus, the answer to be printed out for the length of ”10 7 5 4 3 1” which is 6. Note that the sequence ”9 8 7 5 4 3 1” is not a subsequence we are looking for as the numbers are not contiguous placed in the list. </p>  <br><hr /> <p><b>Input Specification</b></p><br /> <p> Input will contain some positive integers separated by spaces. The number of integers is not specified and the input will be terminated by end of file. Each integer is &lt= 10<sup>9</sup>  and the length of the input sequence is &lt= 10<sup>7</sup>. </p> <br><hr /> <p><b>Output Specification</b></p><br /> <p> Print the length of the longest continuous decreasing subsequence. </p> <br><hr /> <p><b>Sample Input and Output</b></p><br /> <p> Input: 2 1 9 8 10 7 5 4 3 1 10 <br /> Output:6<br /> Input: 1 2 3 4 5 6 7 8<br /> Output: 1<br/> </p> <p align="right"><a href="http://virtual-labs.ac.in/feedback/?lab=cse04">Feedback</a></p> <br><hr /> <br>   <h2>Problem 2:</h2>  <p>  Given a set of n distinct numbers, find the length of the longest monotone increasing subsequence. Note that the sequence need not be continuous(Refer: Section 4.7 of Dromey). </p> <br><hr /> <p><b>Input Specification</b></p><br /> <p> Input will contain two lines. The first line contains the number of integers in the sequence, N(&lt=1000) and the next line contains N positive integers (each &lt=10<sup>9</sup>).  </p> <br><hr />						 <p><b>Output Specification</b></p><br /> <p> Print the length of the longest increasing subsequence of the given input sequence. </p> <br><hr /> <p><b>Sample Input and Output</b></p><br /> <p> Input: 10<br/> 1 2 9 4 7 3 11 8 14 6<br /> Output:6<br /> Input: 6 <br /> 1 3 2 10 4 5<br/> Output: 4<br/> </p> <p align="right"><a href="http://virtual-labs.ac.in/feedback/?lab=cse04">Feedback</a></p> <br><hr />               		
